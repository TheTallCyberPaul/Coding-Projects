# User input
MilesDriven = float(input('Enter the miles driven: ')) # Number of miles driven
GallonsUsed = float(input('Enter the gallons of fuel used: ')) # Number of gallons used

# Math Equations
MilesPerGallon = MilesDriven/GallonsUsed #Calculates miles per gallon

# Print Statements
print(f'Your Mile Per Gallon is {MilesPerGallon:.1f}.')
